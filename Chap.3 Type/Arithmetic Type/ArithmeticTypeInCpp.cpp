int main()
{
    // bool type(true?false). min size: undefined
    bool b;
    // ASCII. min size:8 digit
    char c;
    // wide character. min size: 16 digit
    wchar_t wc_t;
    // Unicode character. min size: 16 digit
    char16_t c16_t;
    // Unicode character. min size: 32 digit
    char32_t c32_t;
    // short integer. min size : 16 digit
    short sint;
    // integer. min size: 16 digit
    int i;
    // long integer. min size: 32 digit
    long l;
    // long integer. min size: 64 digit
    long long ll;
    // single-precision floating-point number
    // min size: 6 significant decimal digits precision
    float f;
    // double-precision floating-point number
    // min size: 10 significant decimal digits precision
    double d;
    // extended-precision floating-point number
    // min size: 10 significant decimal digits precision
    long double ld;
}
